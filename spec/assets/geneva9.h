/*
 * geneva9_glyph -- return pointer to the GENEVA9_CELL_H-byte glyph bitmap
 * for ASCII code c. Returns the blank (space) cell for out-of-range codes.
 *
 * Ref: ADR-0004 D-7 (proportional NFNT text measurement); same pattern as
 * chicago8x16_glyph() (spec/assets/chicago8x16.h).
 */
static inline const unsigned char *geneva9_glyph(int c)
{
    if (c < GENEVA9_FIRST || c > GENEVA9_LAST)
        return geneva9[0]; /* space cell for out-of-range */
    return geneva9[c - GENEVA9_FIRST];
}

/*
 * geneva9_advance_w -- return advance width in pixels for ASCII code c.
 * Returns advance of space for out-of-range codes.
 *
 * Ref: ADR-0004 D-7: "text width = sum of per-glyph advances."
 */
static inline unsigned int geneva9_advance_w(int c)
{
    if (c < GENEVA9_FIRST || c > GENEVA9_LAST)
        return (unsigned int)geneva9_advance[0]; /* space advance */
    return (unsigned int)geneva9_advance[c - GENEVA9_FIRST];
}

